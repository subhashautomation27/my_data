package com.conditionalstatment;

public class IfDemo {
public static void main(String[] args) {
		int i=10;
		int j=20;
	if (i<j)
	{
		System.out.println("j is greater than i");//j is greater than i
	}
	if (i>j)
	{
		System.out.println("i is greater then j");
	}else
	{
		System.out.println("i is not greater than j");//i is not greater than j
	}
	if (i==j)
	{
		System.out.println("i is equal to j");
	}else
	{
		System.out.println("i is not equal to j");//i is not equal to j
	}
	}}

