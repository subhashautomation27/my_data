package com.hello;
public class SwitchDemo {
public static void main(String[] args) {
		int number=10;
		switch (number) {
		case 5:
			System.out.println("5");
		 break;
		case 10:
		     System.out.println("10");
		 break;
		case 20:
			 System.out.println("20");
			 break;
		case 30:
			 System.out.println("30");
			 break;
		default:
			 System.out.println("not in 5,10,20,30");
			 break;}}}


